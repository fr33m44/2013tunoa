DWZ富客户端框架(jQuery RIA framework), 是中国人自己开发的基于jQuery实现的Ajax RIA开源框架.

DWZ富客户端框架设计目标是简单实用扩展方便, 让开发人员不写javascript的情况下, 也能用ajax做项目和使用各种UI组件.

用html扩展的方式取代javascript代码, 只要董html语法, 参考DWZ使用手册就可以使用DWZ组件和ajax.
DWZ框架基本可以保证程序员不董javascript, 也能使用各种页面组件和ajax技术. 如果有一定的javascript功底也可以去扩展DWZ, 做一些定制化开化.

一般做ajax项目时需要写大量的javascript才能达到满意的效果, 这样就比较麻烦. 国内很多程序员javascript也不是太熟, 大大影响了开发速度. 使用DWZ就解决这一问题, 使用DWZ框架自动邦定javascript效果. 不需要开发人员去关心javascript怎么写. 只要写标准html就可以了.

DWZ使用jQuery可以非常方便的定制特定需求的UI组件, 并以jQuery插件的形式发布出来. 如有需要也可做定制化开发.
欢迎大家提出建议, 我们将在下一版本中进一步调整和完善功能．

DWZ富客户端框架是开源项目, 可以免费获取源码. 希望有多的开发人员使用, 共同推进国内整体ajax开发水平.

